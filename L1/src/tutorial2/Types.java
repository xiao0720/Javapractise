package tutorial2;

public class Types {

	public static void main(String[] args) {
		byte o = 0;
		int a = 1;
		short b = 2;
		long c = 3l;
		float d = 4.0f;
		double e = 5.0d;
		char f = 't';
		boolean g = true;

		System.out.println("All the seven data types in java:" + o + a + b + c + d + e + f + g);
		//notice 3l, 4.0f, 5.0d
	}

}
